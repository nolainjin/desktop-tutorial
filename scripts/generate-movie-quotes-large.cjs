function generateMovieQuotes() {
  console.log('🎬 대규모 영화 명대사 생성 시작...\n');

  const movieQuotes = movieQuotesData.map((item, index) => ({
    id: `mq${String(index + 1).padStart(5, '0')}`,
    content: item.quote,
    source: {
      title: item.movie,
      year: String(item.year),
      platform: 'Cinema',
      verified: true
    },
    keywords: item.keywords,
    type: 'movie',
    language: 'ko',
    createdAt: new Date().toISOString()
  }));

  fs.writeFileSync(
    path.join(OUTPUT_DIR, 'movie-quotes.json'),
    JSON.stringify(movieQuotes, null, 2)
  );

  console.log(`✅ 영화 명대사 생성 완료: ${movieQuotes.length}개`);

  // 통계
  const koreanMovies = ['기생충', '올드보이', '살인의 추억', '부산행', '범죄와의 전쟁', '친구', '써니', '건축학개론', '말아톤', '태극기 휘날리며', '베테랑', '광해', '암살', '국제시장', '7번방의 선물', '변호인', '엽기적인 그녀', '타짜', 'JSA', '실미도', '박하사탕', '주먹이 운다', '신과 함께', '명량', '왕의 남자', '도둑들', '택시운전사', '1987', '극한직업', '괴물', '추격자', '곡성', '아가씨', '밀정', '터널', '군함도', '강철비', '마약왕', '특별시민'];

  const korean = movieQuotesData.filter(m => {
    return koreanMovies.some(km => m.movie.includes(km));
  }).length;

  console.log(`   - 한국 영화: ${korean}개`);
  console.log(`   - 해외 영화: ${movieQuotesData.length - korean}개\n`);

  console.log('🎯 특징:');
  console.log('   - 널리 알려진 유명 명대사');
  console.log('   - 정확한 영화 제목과 연도');
  console.log('   - 한국어로 번역된 대사');
  console.log('   - 다양한 장르 (드라마, 액션, SF, 애니메이션, 코미디, 로맨스 등)\n');

  if (movieQuotesData.length < 1000) {
    console.log(`⚠️  목표: 1000개, 현재: ${movieQuotesData.length}개`);
    console.log(`💡 ${1000 - movieQuotesData.length}개 더 필요합니다.\n`);
  }
}

